package json.datastructures;

public class VendorSettings {

	String userSettingId, vendorRadius;

	public String getUserSettingId() {
		return userSettingId;
	}

	public void setUserSettingId(String userSettingId) {
		this.userSettingId = userSettingId;
	}

	public String getVendorRadius() {
		return vendorRadius;
	}

	public void setVendorRadius(String vendorRadius) {
		this.vendorRadius = vendorRadius;
	}
}
